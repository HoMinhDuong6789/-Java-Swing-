    private void setForm() {
        row = tbMain.getSelectedRow();
        News objNews = newsTable.getNewFormRow(row);
        tfIDNews.setText(String.valueOf(objNews.getId()));
        tfTenNews.setText(objNews.getName());
        taMoTa.setText(objNews.getPreviiew_text());
        spNgayDang.setValue(objNews.getDate_create());
        Category objCat = new Category(objNews.getId_cat(), objNews.getCname());
        cbDanhMuc.setSelectedItem(objCat);
        String picture = objNews.getPicture();
        setIcon(picture);
        edChiTiet.setText(objNews.getDetail_text());
        loadCBBDanhMuc(cbDanhMuc, false, objCat);

    }

    private void loadCBBDanhMuc(JComboBox<String> cbDanhMuc, boolean isSearch, Category objCat) {
        cbDanhMuc.setModel(new ComboBoxCat(isSearch, objCat));
    }

    private boolean isValid(String add, News objCat) {
        switch (add) {
            case "Add":
                if ("".equals(objCat.getName())) {
                    JOptionPane.showConfirmDialog(null, "vui long nhap ten tin");
                    return false;
                }
                if ("".equals(objCat.getDetail_text())) {
                    JOptionPane.showConfirmDialog(null, "vui long nhap mo ta");
                    return false;
                }
                break;
            case "Del":
                if (objCat.getId() < 0) {
                    JOptionPane.showMessageDialog(null, "Vui Long chon dong de xoa");
                    return false;
                }
                break;

            case "Edit":
                if ("".equals(objCat.getName())) {
                    JOptionPane.showConfirmDialog(null, "vui long nhap ten tin");
                    return false;
                }
                if ("".equals(objCat.getDetail_text())) {
                    JOptionPane.showConfirmDialog(null, "vui long nhap mo ta");
                    return false;
                }
                break;

        }
        return true;
    }

