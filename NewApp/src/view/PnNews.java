    private void btTiemKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTiemKiemActionPerformed
        TableRowSorter sorter = new TableRowSorter(tbMain.getModel());
        tbMain.setRowSorter(sorter);

        ArrayList<RowFilter<AbstractTableModel, Object>> alFilter = new ArrayList<>();

        String idFind = tfIDTK.getText();
        RowFilter<AbstractTableModel, Object> filterID;
        if (!"".equals(idFind)) {
            filterID = RowFilter.regexFilter("^" + idFind + "$", 0);
        } else {
            filterID = RowFilter.regexFilter(idFind, 0);
        }
        alFilter.add(filterID);

        String tenTK = tfTenTK.getText();
        //System.out.println(tenTK);
        RowFilter<AbstractTableModel, Object> filterName = RowFilter.regexFilter("(?i)" + tenTK, 1);
        alFilter.add(filterName);

// tiem keim theo danh muc
        Category objCat = (Category) cbbDanhMucTK.getSelectedItem();
        if (!"".equals(objCat.getName())) {
            RowFilter<AbstractTableModel, Object> filterCAT = RowFilter.regexFilter("(?i)" + objCat.getName(), 2);
            alFilter.add(filterCAT);
        }

//tim kiem theo thoi gian
        if (spTo.isEnabled()) {
            Timestamp datefrom = ConvertNgayThang.connvertDatetoTimeStamp((Date) spFrom.getValue());
            Timestamp dateto = ConvertNgayThang.connvertDatetoTimeStamp((Date) spTo.getValue());
            RowFilter<AbstractTableModel, Object> filterDateFrom = RowFilter.dateFilter(RowFilter.ComparisonType.AFTER, datefrom, 4);
            RowFilter<AbstractTableModel, Object> filterDateTo = RowFilter.dateFilter(RowFilter.ComparisonType.BEFORE, dateto, 4);
            alFilter.add(filterDateFrom);
            alFilter.add(filterDateTo);
        }

        RowFilter<AbstractTableModel, Object> andFilter = RowFilter.andFilter(alFilter);
        sorter.setRowFilter(andFilter);
        // sorter.setRowFilter(filterID);
    }//GEN-LAST:event_btTiemKiemActionPerformed
